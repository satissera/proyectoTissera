        public void cargarTbl(){
        try {
            //colocamos los invitados en la lista de orden por mesa
            DefaultTableModel modelo = new DefaultTableModel();
            Persona[] lista1 = ListaPersonas.getListaPersonas(); 

            //ArrayList lista2 = ListaPersonasOrdenada.getListaPersonasOrd();
            ArrayList lista2 = new ArrayList<>();
            //le agrego al Arralist vacío que acabo de crear, todas las personas de la lista1
            for (int i=0; i< lista1.length;i++){ 
                lista2.add(new Persona(lista1[i].getNombre(),lista1[i].getNumeroMesa()));
            }
            ListaPersonasOrdenada.ordenarLista(lista2);

            

            modelo.addColumn("");
            modelo.addColumn("Invitados");
            modelo.addColumn("Mesa");        

            for (int i=0; i< lista1.length;i++){
                if(lista1[i] !=null){

                    String[] columna= new String[3];
                    columna[0] = Integer.toString(i+1);//es un contador de invitados
                    columna[1] = lista1[i].getNombre();//colocamos el nombre del invitado
                    columna[2] = Integer.toString(lista1[i].getNumeroMesa());//colocamos el numero de mesa al que corresponde


                    modelo.addRow(columna);


                }

            }




            invMesa.setModel(modelo);
//_________________________________________________________________________________________________________________________________________
       
            //colocamos los invitados en la lista de orden alfabético
            DefaultTableModel modelo2 = new DefaultTableModel();


            
            modelo2.addColumn("");
            modelo2.addColumn("Invitados");
            modelo2.addColumn("Mesa");        
            
            //sabemos que la lista1 tiene la misma cantidad de objetos que lista2

            for (int i=0; i< lista1.length;i++){
                Persona personaAux = (Persona) lista2.get(i);
                if(lista2.get(i) !=null){

                    String[] columna2= new String[3];

                    columna2[0] = Integer.toString(i+1);//es un contador de invitados
                    columna2[1] = personaAux.getNombre();
                    columna2[2] = Integer.toString(personaAux.getNumeroMesa());


                    modelo2.addRow(columna2);

                }
            }
            
            invAlf.setModel (modelo2);
            } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this,"Error: " + ex);
            
            }
                    
                
        }
