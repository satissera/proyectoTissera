        public void borrarPersona(){
            try{
                int row = invMesa.getSelectedRow();
                ListaPersonas.borrarPersonas(row);
                cargarTbl();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this,"Error: " + ex);
            }
        }
        
